// A bartender sold 64 bottles of beer and 23 bottles of whisky. You need to calculate how many total bottles are sold.

public class PrimitiveOperators {
  public static void main(String[] args) {
	  
    int beerSold  = 64;
    int whiskySold = 23;
    int totalSold = beerSold + whiskySold;

    System.out.println("Total bottles sold: " + totalSold);
        
  }
}
